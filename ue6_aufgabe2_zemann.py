"""Fläche eines Rechtecks berechnen"""

# Erklärung
print('Berechnen der Fläche eines Rechtecks')

# Länge und Breite eines Rechtecks abfragen.
länge = int(input('Geben Sie bitte die Länge in cm ein!: '))
breite = int(input('Geben Sie bitte die Breite in cm ein!: '))

fläche = länge * breite
print('Die Fläche beträgt: ' + str(fläche) + ' cm^2')
